package codeforwin;

public class random {

    public static void main(String[] args) {
        
        int i = 1;  // Initialize the loop variable
        while (i <= 100) {  // Loop until i is greater than 100
            if (i % 2 == 0) {  // Check if i is even
                System.out.println(i);  // Print i if it is even
            }
            i++;  // Increment i
        }
        
    }
    
}
